      public void mostrarProdutosVendidos(JTable tabela) {
        try {
            DefaultTableModel model = (DefaultTableModel) tabela.getModel();
            model.setNumRows(0); // Limpa a tabela

            ProdutosDAO produtosDAO = new ProdutosDAO();
            ArrayList<ProdutosDTO> listaVendidos = produtosDAO.listarProdutosVendidos();

            for (ProdutosDTO produto : listaVendidos) {
                    model.addRow(new Object[]{
                    produto.getId(),
                    produto.getNome(),
                    produto.getValor(),
                    produto.getStatus()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao mostrar produtos vendidos: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

   
